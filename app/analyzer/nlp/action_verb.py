"""Функции для проверки наличия глаголов действия в тексте кнопок."""

async def has_action_verb(button_text: str) -> bool:
    """
    Проверяет, содержит ли текст кнопки глагол действия (упрощённо).
    Можно расширить с помощью pymorphy2 или spaCy.
    """
    action_words = ["отправить", "сохранить", "удалить", "редактировать", "добавить", "купить", "войти", "зарегистрироваться", "отмена", "назад"]
    return any(word in button_text.lower() for word in action_words)